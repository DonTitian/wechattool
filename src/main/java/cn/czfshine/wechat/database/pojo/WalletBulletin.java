package cn.czfshine.wechat.database.pojo;


public class WalletBulletin {

  private String bulletinScene;
  private String bulletinContent;
  private String bulletinUrl;


  public String getBulletinScene() {
    return bulletinScene;
  }

  public void setBulletinScene(String bulletinScene) {
    this.bulletinScene = bulletinScene;
  }


  public String getBulletinContent() {
    return bulletinContent;
  }

  public void setBulletinContent(String bulletinContent) {
    this.bulletinContent = bulletinContent;
  }


  public String getBulletinUrl() {
    return bulletinUrl;
  }

  public void setBulletinUrl(String bulletinUrl) {
    this.bulletinUrl = bulletinUrl;
  }

}
