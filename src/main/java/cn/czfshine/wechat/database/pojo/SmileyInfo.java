package cn.czfshine.wechat.database.pojo;


public class SmileyInfo {

  private String key;
  private String cnValue;
  private String qqValue;
  private String twValue;
  private String enValue;
  private String thValue;
  private String fileName;
  private long eggIndex;
  private long position;
  private long flag;


  public String getKey() {
    return key;
  }

  public void setKey(String key) {
    this.key = key;
  }


  public String getCnValue() {
    return cnValue;
  }

  public void setCnValue(String cnValue) {
    this.cnValue = cnValue;
  }


  public String getQqValue() {
    return qqValue;
  }

  public void setQqValue(String qqValue) {
    this.qqValue = qqValue;
  }


  public String getTwValue() {
    return twValue;
  }

  public void setTwValue(String twValue) {
    this.twValue = twValue;
  }


  public String getEnValue() {
    return enValue;
  }

  public void setEnValue(String enValue) {
    this.enValue = enValue;
  }


  public String getThValue() {
    return thValue;
  }

  public void setThValue(String thValue) {
    this.thValue = thValue;
  }


  public String getFileName() {
    return fileName;
  }

  public void setFileName(String fileName) {
    this.fileName = fileName;
  }


  public long getEggIndex() {
    return eggIndex;
  }

  public void setEggIndex(long eggIndex) {
    this.eggIndex = eggIndex;
  }


  public long getPosition() {
    return position;
  }

  public void setPosition(long position) {
    this.position = position;
  }


  public long getFlag() {
    return flag;
  }

  public void setFlag(long flag) {
    this.flag = flag;
  }

}
