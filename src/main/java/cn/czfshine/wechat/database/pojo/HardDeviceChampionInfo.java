package cn.czfshine.wechat.database.pojo;


public class HardDeviceChampionInfo {

  private String username;
  private String championUrl;
  private String championMotto;


  public String getUsername() {
    return username;
  }

  public void setUsername(String username) {
    this.username = username;
  }


  public String getChampionUrl() {
    return championUrl;
  }

  public void setChampionUrl(String championUrl) {
    this.championUrl = championUrl;
  }


  public String getChampionMotto() {
    return championMotto;
  }

  public void setChampionMotto(String championMotto) {
    this.championMotto = championMotto;
  }

}
