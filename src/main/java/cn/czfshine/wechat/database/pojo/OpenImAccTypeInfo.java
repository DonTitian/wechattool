package cn.czfshine.wechat.database.pojo;


public class OpenImAccTypeInfo {

  private String acctTypeId;
  private String language;
  private String accTypeRec;
  private String updateTime;


  public String getAcctTypeId() {
    return acctTypeId;
  }

  public void setAcctTypeId(String acctTypeId) {
    this.acctTypeId = acctTypeId;
  }


  public String getLanguage() {
    return language;
  }

  public void setLanguage(String language) {
    this.language = language;
  }


  public String getAccTypeRec() {
    return accTypeRec;
  }

  public void setAccTypeRec(String accTypeRec) {
    this.accTypeRec = accTypeRec;
  }


  public String getUpdateTime() {
    return updateTime;
  }

  public void setUpdateTime(String updateTime) {
    this.updateTime = updateTime;
  }

}
