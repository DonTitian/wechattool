package cn.czfshine.wechat.database.pojo;


public class Stranger {

  private String encryptUsername;
  private String conRemark;
  private String contactLabels;
  private String conDescription;
  private String conPhone;


  public String getEncryptUsername() {
    return encryptUsername;
  }

  public void setEncryptUsername(String encryptUsername) {
    this.encryptUsername = encryptUsername;
  }


  public String getConRemark() {
    return conRemark;
  }

  public void setConRemark(String conRemark) {
    this.conRemark = conRemark;
  }


  public String getContactLabels() {
    return contactLabels;
  }

  public void setContactLabels(String contactLabels) {
    this.contactLabels = contactLabels;
  }


  public String getConDescription() {
    return conDescription;
  }

  public void setConDescription(String conDescription) {
    this.conDescription = conDescription;
  }


  public String getConPhone() {
    return conPhone;
  }

  public void setConPhone(String conPhone) {
    this.conPhone = conPhone;
  }

}
